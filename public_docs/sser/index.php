<h1>A 'Scottish School of Educational Research', 1925-1950</h1>
	
<a href="http://www.flickr.com/photos/edinburgh-university-archives/5013929173/" title="Sir Godfrey H Thomson by Edinburgh University Archives, on Flickr"><img align="right" border="0" src="http://farm5.static.flickr.com/4091/5013929173_15c35bfe07_m.jpg" width="169" height="240" alt="Sir Godfrey H Thomson" /></a>
<p>In January 2006 Special Collections became involved in an ESRC-funded project which aimed to construct an intellectual history of education and the intelligence concept, technology and practice in Scotland between 1925 and 1950, and demonstrate the profound and enduring influences of this Scottish School on world educational and psychological research. The focus of the study was on the specific locations and cultural contexts of scientific research; the exchange in international networks of ideas, methods and results; the intellectual history of key individuals, especially Professor Sir Godfrey Thomson, Principal of Moray House College of Education and Bell Professor of Education in the University of Edinburgh (pictured, right); the relation between statistics and text, and between scientific advance and the governing of education systems.</p>

<p>The researchers on the project are experts in the history of education (Professor Martin Lawn, School of Education, University of Edinburgh), in intelligence testing (Professor Ian Deary, Psychology, University of Edinburgh), and the statistics of mental tests (Professor David Bartholomew, London School of Economics).</p>

<p>The researchers were utilising archival materials recently located within the University to help reconstruct the Scottish School. Recognising the vital need to permanently preserve these materials and ensure they are accessible into the future, project funds enabled the Special Collection's Project Archivist to be seconded to the project. The Project Archivist's role was to survey the material; oversee transfer to Special Collections; assess and then process the material accordingly. This included basic preservation measures, fonds and subfonds level cataloguing, and creation of detailed handlists. Relevant collections and materials already deposited with Special Collections have also received preservation and descriptive treatment as part of the project. Finally, funds also allowed for professional conservation of the most vulnerable material.</p>

<p>We continue to work with our academic collegues, building on the success of this project, identifying further archival material of relevance and working towards a follow-up project.</p>

