<h1>The project</h1>

<p>'Towards Dolly', based at the University of Edinburgh's Centre for Research Collections, is a project which catalogues, preserves and makes publicly available archival, printed and visual collections relating to animal genetics in Edinburgh, from nineteenth-century natural history to the birth of Dolly the sheep in 1996.  The project incorporates 'The Making of Dolly: Science, Politics and Ethics', which is currently cataloguing archival collections relating to animal genetics, including the papers of Professor Sir Ian Wilmut, Professor Grahame Bulfield and the institutional records of the Roslin Institute and its predecessor bodies.</p>
<p>These projects are generously funded by the Wellcome Trust's Research Resources scheme. Watch the Wellcome's film about the scheme, which includes Project Archivist, Clare Button, talking about the collections in this project.</p>

