<h2>What is Catalogued?</h2>

<p>Not all of what we hold is catalogued.  Where possible we have created at least a catalogue record for a collection as a whole.  We are constantly creating new catalogue records, sometimes as part of core work but more often through project work.  Staff often have knowledge of currently uncatalogued material so it is often worthwile asking even if you don't find what you are looking for in the catalogue.</p>

