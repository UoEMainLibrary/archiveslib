<a name="refcodes"></a>
<h2>Reference codes and Shelfmarks</h2>
<p>Today we catalogue to accepted international archival standards, including assigning of reference codes.  In earlier periods most archive and manuscripts were simply identified by their library shelfmark (denoting where they were shelved).  Some of these persist to this day, particularly where cataloguing has still to be done to any level of detail.</p>

<h3>Reference codes</h3>

<p>Each collection has a unique reference code, usually prefixed 'Coll' or 'EUA'.  Items within the collection will be referenced as sub numbers (where lower level cataloguing has been carried out). Due to some historically anomalies, we currently have some material grouped together as collections which are no more than loosely associated items.  This will be rectified but, in order tht these items can still be found in the catalogue, these collection descriptions have been left in place for the moment.</p>

<h3>Shelfmarks</h3>

<p>Historically these collections were managed in a way that parallelled how printed collections were managed, where what something was intellectually was almost synonymous with where it was physically shelved.  Many of these shelfmarks persist to this day and, are still used to manage physical locations and to request material for consultation.  The main shelfmark sequences you may see in catalogue records are:

