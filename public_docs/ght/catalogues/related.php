      <li>From PSY/SPE, <strong>Papers of Charles Spearman</strong>: 
      <ul>
      <li>PSY/SPE/1/1/1, ‘Traits of character’ - Thomson’s ‘Bonds’ theory is alluded to.</li>
      <li>PSY/SPE/1/2/26, ‘Notes from Kelley’s method’ - Includes notes compiled by Spearman regarding Thomson’s paper from the British Journal of Psychology, ‘On complete families of correlation coefficients and their tendency to zero tetrad differences: including a statement of the sampling theory of abilities’,1935.</li>
      <li>PSY/SPE/1/4/10, ‘Correspondence: Garnett, Maxwell’ - Includes three letters with reference to Thomson’s work, specifically his ‘Bonds’ theory: letter from Spearman to Garnett, in which Spearman suggests Garnett is undecided between his and Thomson’s arguments, 23 Nov 1919; Garnett’s reply to this, in which he stresses his acceptance of the ‘Two Factor’ theory, 23 Nov 1919; and a letter from Garnett to Spearman regarding the latter’s manuscript of a reply to Thomson, in which Garnett praises Spearman’s work and dismisses Thomson’s work, 18 Feb 1927.</li>
      <li>PSY/SPE/1/4/24, ‘Correspondence: Thomson, G’ - Despite the title, contains no letters to or from Thomson. Comprises of a typescript of Spearman’s article &quot;Is ability random or organized?&quot; and some manuscript statistical notes by Spearman.</li>
      <li>PSY/SPE/1/6/10, ‘Correspondence: Kelley, Truman L’, 1922-1926 - Includes a letter from Truman L Kelley regarding his review of Brown and Thomson’s Mental Measurement, and Spearman’s objections. Kelley argues that Spearman has not fully answered Thomson’s criticisms of his ‘Two Factor’ theory. Kelley makes oblique references to Thomson in two further letters, suggesting he had been mistaken in thinking the two had reached a mutual agreement as with Spearman and Thorndike.</li>
      <li>PSY/SPE/2/6, ‘Reference material 'Vol. I S-T': Sander – Tswardowski’ - This contains typed notes regarding Thomson’s ‘Theory of correlation of abilities’.</li>
      </ul>
      </li>
      
    </ul>
