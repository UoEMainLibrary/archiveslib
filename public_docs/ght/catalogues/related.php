<h2>University of Adelaide Special Collections, Adelaide, Australia</h2>

<ul>
	<li>MSS 0013, <strong>Papers of Sir Ronald Aylmer Fisher</strong> (1890-1962), statistician and geneticist, University of Adelaide Special Collections
	  Include copies of letters to Thomson from Fisher, with one reply from Thomson. These largely regard intelligence testing and measuring methodology; Otis tests; Fisher’s methods of estimation; and Thomson’s membership of the International Statistical Institute following Fisher’s nomination.
	  Many of these have been digitised and can be found online.</li>
</ul>

