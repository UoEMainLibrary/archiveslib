<h2>Durham University Library, Special Collections (GB 33)</h2>
<ul>
<li> DUF 3/B, <strong>Papers of James Fitzjames Duff</strong>: There are several items of interest in these papers, particularly those regarding the work Thomson and Duff carried out testing of elementary school children in Northumberland, which includes correspondence with tables of results, annotations, and suggestions for statistical analysis.  The correspondence between Thomson and Duff explores ways in which the tests might be followed up and the testing and measuring methodology for doing so. Duff and Thomson also discuss how to encourage both parents and teachers to co-operate, and how parents might see the testing as beneficial.  The collection also contains several letters in response to Duff’s editorial work on the Education of an Englishman, from correspondents such as Cyril Burt, W B Inglis, Jenny Lee, and Hector Thomson.</li>
<li>MIA 1-4, <strong>Photograph albums of Lieutenant-Colonel Alexander Angus Macfarlane-Grieve</strong>: MacFarlane-Greive was a colleague of Thomson’s at Armstrong College. Photographs of MacFarlane-Greive’s sporting and military activities feature heavily in the albums – both he and Thomson were in the Officers Training Corps, and Thomson features in his albums.</li>


</ul>

