<h2>Carnegie Collections Rare Book and Manuscript Library, New York, USA</h2>
<ul>
<li>NYCR91-A6, <strong>Records of the Carnegie Corporation of New York</strong> - The Carnegie Corporation of New York supported the International Examinations Inquiry and the collection contains several files which provide contextual information about the Scottish Mental Survey, and Scotland’s contribution to the enquiry. Items of particular interest include:
	<ul>
    <li>Series III A, 78. 9, ‘Carnegie foundation for Advancement in Teaching – support of Research in Factorial Analysis (Godfrey Thomson), 1937-1939’.  Contains material regarding the award of a £5000 research grant to Thomson, such as correspondence, memoranda, and cross-reference sheets, largely between Thomson, F P Keppel, E L Thorndike, and Thomas S Arbuthnot. The grant was endorsed by Thorndike and Arbuthnot, and their esteem for Thomson and his work is clear throughout, with reference being made to Thorndike describing him as ‘tops’. The file gives information about the processes involved in both carrying out the research, in Thomson obtaining a leave of absence from the University of Edinburgh, and the hiring of his research assistant, Walter Ledermann.</li>
    <li>The collection also includes four files of the International Examinations Inquiry 1930-1945. These give a great deal of information regarding each country’s work, with reports and correspondence regarding their progress and methodology. Includes a letter to Thomson from Keppel requesting the former’s involvement in the inquiry, and a letter from Howard Savage (Secretary of the Carnegie Foundation) mentioning how well the Scottish team were progressing.</li>
    </ul>
</li>
</ul>

