        <p>Geddes was a pioneer of the environmental movement. He developed a deep fascination with the organization of human societies and their spatial manifestation in the city and the country, propagating a highly individualistic theory of societies and cities drawing from regional theories in biology and geography, philosophical ideas (especially those of Plato) and political anarchist thought.  Credited as the father of town planning, his approach was firmly rooted in how people and environment interact, seeing cities as organic entities.  People, what they do and the places they inhabit need to be in equilibrium, hence the triad 'folk, work, place'.  He believed in the interconnections between all branches of knowledge and was a true polymath.</p>
<p>He established the Outlook Tower on Castle Hill, Edinburgh. In turn it became custodian to a substantial collection of his papers.  In October 1955, the Royal Technical College in Glasgow, acquired a larger collection of Geddes papers from Arthur Geddes (son of Patrick). This forms the collection held at Strathclyde University.</p>
		
        
		<h2>The collections</h2>
		<h3>Edinburgh</h3>
		<p>The Papers of Patrick Geddes from the Outlook Tower include material relating to: the Cities and Towns Planning Exhibition, The Survey of Edinburgh (photographs); prints of buildings and townscapes in Scotland; work in India (including work by Arthur Geddes); work in Palestine; prints and drawings of the Middle East; and family photographs and artefacts.</p>
        <p>The collection of Sir Frank Mears papers relates to his collaboration with Geddes, principally (but not exclusively) in relation to design of the New Hebrew University in Jerusalem.</p>
		<h3>Strathclyde</h3>
		<p>The collection at Strathclyde is extensive. It covers most periods of Geddes’ life, and nearly all of the places in which he lived and worked. It comprises Geddes’ own notes and papers on a wide variety of topics including town and regional planning, economics, sociology, history, art, religion, philosophy, education, geography and biology as well as his correspondence with thousands of correspondents from all over the world. These correspondents include many of the leading figures of the time. The collection also includes thousands of maps, plans, prints, drawings, photographs and glass slides, from the 16th century to the 20th century, which Geddes collected as background material for his city surveys. This part of the collection is particularly strong for cities in the British Isles, France, India and Palestine, the countries where Geddes lived and worked, but there is considerable material relating to many other countries too. Many of these maps, plans, prints, drawings and photographs were part of Geddes’ Cities and Town Planning Exhibition.</p>
       
        <h3>Elsewhere</h3>
        <p>We are aware of substantial tranches of Geddes papers elsewhere.  These will not form part of this project but contact will be made with host repositories, in order to map what is where and to discuss options for the future.</p>
        
       
