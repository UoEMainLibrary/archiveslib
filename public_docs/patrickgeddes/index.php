       <h2>About the project</h2>
		<p>This project aims to catalogue, digitise and conserve the two major collections of Patrick Geddes papers at the Universities of Edinburgh and Strathclyde as the first step towards a more encompassing project which will open up the collections for research.  This research is likely to be as broad and diverse in activity as was Geddes himself.</p>
        <p>At the moment we are preparing a scoping exercise with a view to applying for funding early next year.  If we are successful, we would hope to begin the project itself later in 2015.</p>
        
