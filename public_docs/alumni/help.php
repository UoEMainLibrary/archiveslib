<h2>Matriculation and Graduation Practices</h2>

	<p>The surviving records of Edinburgh University students between 1583 (when the University was established) and the present day vary greatly.  Until the 1730s, the only evidence of a student's presence is a signature in the matriculation or enrolment books, dating from 1627, and, if the student graduated, a signature in the graduation album, dating from 1587.</p>

	<p>Edinburgh is unique amongst the ancient Scottish Universities in not systematically recording students' parent's names, with place and date of birth at enrolment until the early 1920's.  This means that those with the more common Scottish name combinations such as John Robertson, or James Stewart, are virtually indistinguishable from one another.  It is also worth noting that for much of the period between 1700 and 1858 and sometimes later, many students did not bother to go through the formalities of matriculation and especially graduation.  Instead, they would attend various classes and if necessary, for future employment purposes, obtain a class certificate direct from individual Professors.  Arts students (and this designation included science, mathematics and agriculture), rarely graduated at all in the 200 years up to 1900.  Consequently, whilst a student may have attended Edinburgh University in this period, it does not follow that he/she will be on record.</p>

