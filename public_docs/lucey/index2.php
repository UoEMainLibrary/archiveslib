<h2>The Collection</h2>

<p>His  extensive collection of films include those about or featuring leading medics  and scientists such as Conrad Hal Waddington, Dorian Pritchard, Christopher  Pennington and Charlotte Auerbach as well as films focusing on specific  teaching and research issues (e.g. 'Amphibian Dissection' or 'Optomotor  Responses in Cephalotods').  Alongside  these sit a smaller number of films of a slightly more general nature, somewhat  contextualising Lucey’s work and the broader landscape in which the Institute of Animal Genetics operated.  Some of Lucey’s films won international  awards, such as <em>Shoreline Sediments</em> which  attained first prize in the Geography/Geology section of the Fifth  International Festival of Technical and Scientific Films in 1970. The collection consists predominantly of various gauges of open-reel films and accompanying audio tape (with smaller quantities of video cassettes and folders of photographs and papers).</p>
  
