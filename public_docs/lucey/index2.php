<h2>Historical Context</h2>
 
<p>In 1950  Eric C. A. Lucey (1923-2010) suggested to Professor Waddington that a Research  Film Unit be established within the Institute for Animal Genetics.  His request was granted by an enthusiastic  Waddington and Lucey was able to follow the recommendations of a number of  contemporary reports that were calling for an increased use of film in research  and teaching.  The Unit remained a one  man operation until Lucey’s early retirement in 1989.</p>


