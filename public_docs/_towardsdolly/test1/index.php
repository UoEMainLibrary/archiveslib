  	<h2>Aims and<br />
<span>Objectives</span></h2>
<p>This project will catalogue, preserve and conserve five internationally-important collections which tell the story of how Edinburgh became one of the world's centres of genetics research.</p>
<p class="largegraytext">"Spanning as they do from before the rediscovery of Mendel's work to the birth of Dolly and stem cell research, these papers form a collection unrivalled in the history and development of Genetics." Prof Graham Bullfield</p>
