<h2>Edinburgh, Roslin and the birth of Modern Genetics</h2>
<p class="toptextpadding">Towards Dolly is a <a href="#">Wellcome Trust</a> funded project based at the <a href="#">University of Edinburgh's Centre for Research Collections</a>. Through cataloguing and preservation we will make accessible to the public collections which docuemnt Edinburgh's key position in the development of genetics.</p>
