<p>The student records held by Edinburgh University Archives hold content of immense value for the amateur and professional researcher alike.  While most enquiries relating to these records are of a genealogical or biographical nature, there remains significant potential for statistical analysis of the data they contain for historical, sociological and other research.</p>

<p>With generous funding from both <a href="http://www.general-council.ed.ac.uk/" title="Link to General Council website" target="_blank">General Council</a> and the <a href="http://www.lib.ed.ac.uk/about/friends/" title="Link to Friends' website in a new window" target="_blank">Friends of Edinburgh University Library</a>, a scoping project has been undertaken and report submitted which will inform the next stages.  While the entire report cannot be shared online, the results of the records survey can.</p>

